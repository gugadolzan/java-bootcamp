package example03;

public class Example03 {

    public static void main(String[] args) {
        int count = 1;

        while (count <= 10) {
            System.out.print(count + " ");
            count++;
        }

        System.out.println();

        for (count = 1; count <= 10; count++) {
            System.out.print(count + " ");
        }
    }

}
